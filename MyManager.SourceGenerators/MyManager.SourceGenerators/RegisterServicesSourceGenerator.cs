using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace MyManager.SourceGenerators;

[Generator]
public sealed class RegisterServicesSourceGenerator : IIncrementalGenerator
{
    private const string Namespace = "MyManager.Attributes";
    private const string RegisterScopedAttribute = "RegisterScopedAttribute";
    private const string RegisterSingletonAttribute = "RegisterSingletonAttribute";
    private const string RegisterTransientAttribute = "RegisterTransientAttribute";

    private const string TargetNamespace = "MyManager.Services";
    private const string TargetClassName = "RegisterServicesExtensions";

//     private const string AttributesSource =
